//== double equals
//it is used to compare values, it does not the check the type of values so it is 
// loose comparison

 let check = 2 == '2'
 console.log(check)  //true


// === triple equals
//it is used to compare values and also check the type of values so it is strong comparison
let check1 = 2 === '2'
 console.log(check1)// false










